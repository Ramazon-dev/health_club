class District {
  final String name;

  District({required this.name});
}

class Region {
  final String name;
  final List<District> districts;

  Region({required this.name, required this.districts});

  factory Region.fromMap(String regionName, List<String> districtNames) => Region(
    name: regionName,
    districts: districtNames.map((d) => District(name: d)).toList(),
  );
}

